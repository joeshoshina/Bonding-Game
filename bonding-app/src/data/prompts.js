export const getRandomPrompt = (level) => {
  const levelPrompts = prompts[level];
  if (!levelPrompts) return "No prompts available for this level.";
  const randomIndex = Math.floor(Math.random() * levelPrompts.length);
  return levelPrompts[randomIndex];
};

